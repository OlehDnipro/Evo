#pragma once

enum ItemType
{
	CONSTANT,
	TEXTURE,
	STRUCTUREDBUFFER,
	RWTEXTURE,
	RWSTRUCTUREDBUFFER,
	CBV,
	SMP,
	RESOURCE_TABLE,
	SAMPLER_TABLE,

	INVALID = -1,
};

struct SRootSlot
{
	ItemType m_Type;
	uint32   m_Size;
};

struct SResourceTableItem
{
	ItemType m_Type;
	uint32   m_NumElements;
};

struct SRoot
{
	uint32 m_NumSlots;
	SRootSlot m_Slots[1]; // Will actually contain m_NumSlots elements
};
typedef void (*RootCallback)(ItemType type, uint table, uint binding, uint table_creation_size, void* receiver);
